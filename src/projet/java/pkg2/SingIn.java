    private void jLogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLogActionPerformed
        // TODO add your handling code here:
        try {
            //Charger le Pilote
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gestion_hygene", "root", "");

            //Recuperer le elements des champs respectifs
            String Username = TUsername.getText();
            String Password = TPassword.getText();
            String ConfimPassword = TConfigPassword.getText();
            if (Password == ConfimPassword) {
                // Comparer avec les donnees de la BD
                PreparedStatement st = con.prepareStatement("SELECT*FROM TUser WHERE Username=? AND Password=? ");
                st.setString(1, Username);
                st.setString(2, Password);
                ResultSet rs = st.executeQuery();
                if (rs.next() == true) {
                    SingIn sing = new SingIn();
                    this.setVisible(false);
                    sing.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Creation de compte echoue");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jLogActionPerformed

